package dolandre_Febrianto;

public class Day11_array_menggunakan_looping_each {

    public static void main(String[] args) {
        String[] alatTulis = {"Pulpen", "Buku", "Pensil", "Penghapus", "Penggaris"};

        // mencetak menggunakan looping for each 
        for (String a : alatTulis) {
            System.out.println(a + ";");
        }
        System.out.println();
    }

}
